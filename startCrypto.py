def main():
    print(os.path.join(os.getcwd(), ".crypt"))
    gpg = gnupg.GPG(gnupghome=os.path.join(os.getcwd(), ".crypt"), use_agent=False)
    gpg.encoding = "utf-8"
    inputdata = gpg.gen_key_input(key_type="RSA", key_length=2048, name_real="Config Crypto",
                                  name_comment="Config Crypto", name_email=mail, passphrase=passwd)  # 2048 is max
    gpg.gen_key(inputdata)


if __name__ == '__main__':
    main()
